// MPA 장점
// 1. 첫 페이지 로딩 시간이 빠름
// 2. 검색 엔진 (SEO) 최적화

// MPA 단점
// 1. 페이지 이동 속도 느림
// 2. 화면이 깜빡이는 듯한 현상
// 3. 동일한 코드 중복 작성

